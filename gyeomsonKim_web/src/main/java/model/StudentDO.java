package model;

public class StudentDO {
	private String stdnum;
	private String stdname;
	private String stdgender;
	private String stdkor;
	private String stdeng;
	private String stdmat;
	private String stdsci;

	public StudentDO() {
	}

	public String getStdnum() {
		return stdnum;
	}

	public void setStdnum(String stdnum) {
		this.stdnum = stdnum;
	}

	public String getStdname() {
		return stdname;
	}

	public void setStdname(String stdname) {
		this.stdname = stdname;
	}

	public String getStdgender() {
		return stdgender;
	}

	public void setStdgender(String stdgender) {
		this.stdgender = stdgender;
	}

	public String getStdkor() {
		return stdkor;
	}

	public void setStdkor(String stdkor) {
		this.stdkor = stdkor;
	}

	public String getStdeng() {
		return stdeng;
	}

	public void setStdeng(String stdeng) {
		this.stdeng = stdeng;
	}

	public String getStdmat() {
		return stdmat;
	}

	public void setStdmat(String stdmat) {
		this.stdmat = stdmat;
	}

	public String getStdsci() {
		return stdsci;
	}

	public void setStdsci(String stdsci) {
		this.stdsci = stdsci;
	}

}
